<h1>Uw visie</h1>
<p><img class="alignright" src="/upload/visie_floortje.jpg" alt="Visie" width="300" height="237">Daarmee bedoelen wij dat wij uw paarden kunnen onderhouden op de manier die U wenst. Wij zijn flexibel om dat gene te doen dat u van ons verlangt voor uw paard.</p>
<p>In de zomermaanden staan de paarden in goed bijgehouden weilanden, welke regelmatig worden vernieuwd. s'Nachts gaan de paarden weer naar hun eigen stal.&nbsp;De beweiding van de paarden vindt plaats in een omweidesysteem (vaak wisselen van perceel). De ruwvoederwinning voor de winterperiode vindt hoofdzakelijk plaats in de vorm van grassilage (voordroogkuil) . Dit is half gedroogd gras (40-50% droge stof) dat afgesloten van lucht wordt bewaard. De weilanden zijn omheind met elektrische afrastering.</p>
<p>In de winter staan de paarden in de stal. De paarden tot drie jaar staan in groepen van maximaal 8 dieren. Deze manier van huisvesting heeft belangrijke voordelen ten opzichte van individuele huisvesting in boxen. Het frisse stalklimaat heeft een gunstige invloed op de gezondheid van uw paarden. Bovendien hebben de paarden relatief veel bewegingsvrijheid en contact met soortgenoten. De paarden krijgen onbeperkt ruwvoer verstrekt. Het rantsoen van de veulens wordt aangevuld met ongeveer 2 kg krachtvoer per dag. Het grootste deel van het ruwvoer wordt zelf gewonnen en wordt bewaard in plastic folie gewikkelde balen. Als strooisel in de stallen wordt gebruik gemaakt van tarwestro. Het drinkwater is voorzien van&nbsp;een rondpompsysteem&nbsp;die het water in de leidingen verwarmt, zodat bij vorst de drinkwaterverstrekking aan de paarden niet in het geding komt. De paarden komen dagelijks in de paddock die naast de stal is gelegen. De groepen worden apart naar buiten gelaten in de paddock.&nbsp;Voor paarden ouder dan 3 jaar is het&nbsp;mogelijk om uw paard te stallen in een van onze individuele paardenboxen.</p>
<p>Voor de opfok van uw paard hanteren wij een aparte prijs voor zowel de zomer- als de wintermaanden. De prijzen zijn exclusief hoefsmid- dierenarts- en entkosten.</p>
<p>Voor meer informatie inzake de prijzen van het opfokken van uw jonge paard/pony of het stallen van uw paard/pony kunt u met ons contact opnemen.</p>
